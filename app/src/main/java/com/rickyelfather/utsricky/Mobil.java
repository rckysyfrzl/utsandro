package com.rickyelfather.utsricky;

public class Mobil {
    private String nama;
    private String spek;
    private int poto;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getSpek() {
        return spek;
    }

    public void setSpek(String spek) {
        this.spek = spek;
    }

    public int getPoto() {
        return poto;
    }

    public void setPoto(int poto) {
        this.poto = poto;
    }
}
